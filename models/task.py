class Task:
    def __init__(self, id, title,  description, completed=False):
        self.id = id,
        self.title = title, 
        self.description = description,
        self.completed = completed

        def exibir_dados(self):
            return {
                "id": id.self,
                "title": title.self,
                "description": description.self,
                "completed": completed.self
            }
            